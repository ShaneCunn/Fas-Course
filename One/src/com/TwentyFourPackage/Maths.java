package com.TwentyFourPackage;

public class Maths {

	void printMe(int[] myArray) {

		for (int b : myArray) {

			System.out.println("int is: " + b);
		}
	}

	void printMe(String[] myArray) {

		for (String b : myArray) {

			System.out.println("String is :" + b);
		}
	}

	void printMe(double[] myArray) {

		for (double b : myArray) {

			System.out.println("double is:" + b);
		}
	}

}

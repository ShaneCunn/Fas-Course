package com.Two.Four;

public class Examples {

	static void assignmentOper() {

		int a = 12, b = 3;

		a -= b; // same as a= a-b;
		System.out.println("a is " + a + " b is =" + b);

		b *= a;// same as b= b*a;
		System.out.println("a is " + a + " b is =" + b);

		a += b; // same as a= a+b;

		System.out.println("a is " + a + " b is =" + b);
	}
}
